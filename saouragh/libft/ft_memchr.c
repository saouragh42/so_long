#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	uc;
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = (unsigned char *) s;
	uc = (unsigned char) c;
	while (i < n)
	{
		if (p[i] == uc)
			return ((void *)(p + i));
		i++;
	}
	return (NULL);
}
